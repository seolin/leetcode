public class T300 {
    public static void main(String[] args) {
        int[] nums = {10, 9, 2, 5, 3, 4};
        System.out.println(new T300().lengthOfLIS3(nums));
    }

    public int lengthOfLIS3(int[] nums) {
        int[] tails = new int[nums.length];
        int res = 0;
        for (int num : nums) {
            int i = 0, j = res;
            while (i < j) {
                int m = (i + j) >> 1;
                if (tails[m] >= num) {
                    j = m;
                } else {
                    i = m + 1;
                }
            }
            tails[i] = num;
            if (res == i) {
                res++;
            }
        }
        return res;
    }

    public int lengthOfLIS(int[] nums) {
        int n = nums.length;
        int[] tails = new int[n];

        int res = 0;
        for (int num : nums) {
            int i = 0, j = res;
            while (i < j) {
                int m = (i + j) >> 1;
                if (tails[m] <= num) {
                    i = m + 1;
                } else {
                    j = m - 1;
                }
            }
            tails[i] = num;
            if (res == j) {
                res++;
            }
        }
        return res;
    }

    public int lengthOfLIS2(int[] nums) {
        int[] tails = new int[nums.length];
        int res = 0;
        for (int num : nums) {
            int i = 0, j = res;
            while (i < j) {
                int m = (i + j) / 2;
                if (tails[m] < num) {
                    i = m + 1;
                } else {
                    j = m;
                }
            }
            tails[i] = num;
            if (res == j) {
                res++;
            }
        }
        return res;
    }

}
